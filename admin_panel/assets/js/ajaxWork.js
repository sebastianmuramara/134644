//add product data
function addItems(){
    var s_id=$('#s_id').val();
    var clas=$('#clas').val();
    var s_name=$('#s_name').val();
    var u_name=$('#u_name').val();
    var p_word=$('#p_word').val();
    var age=$('#age').val();
    var g_level=$('#g_level').val();

    var fd = new FormData();
    fd.append('s_id', s_id);
    fd.append('clas', clas);
    fd.append('s_name', s_name);
    fd.append('u_name', u_name);
    fd.append('p_word', p_word);
    fd.append('age', age);
    fd.append('g_level', g_level);
    $.ajax({
        url:"./controller/addItemController.php",
        method:"post",
        data:fd,
        processData: false,
        contentType: false,
        success: function(data){
            alert('Student Added successfully.');
            $('form').trigger('reset');
            showProductItems();
        }
    });
}

